//Box Model
  // Each HTML element is considered a rectangular box, and the box model defines how these boxes are structured, including their content, padding, border, and margin. 
//Display:
 //In CSS, the display property is used to control how an HTML element is displayed. It determines the type of box the element generates and how it interacts with other elements in the layout. 
//Inline-block
  //inline-block is a value for the display property in CSS. It combines features of both inline and block elements, allowing an element to flow like an inline element while maintaining block-level properties.

//Relative Units
  //In CSS, relative units are used to specify a length or size relative to some other property. Relative units are particularly useful for creating designs that are more adaptable and responsive to different screen sizes and devices.
  //em
   //The em unit is relative to the font-size of the nearest parent or the element itself. If no font-size is specified, the em is relative to the font-size of the closest ancestor with a specified font-size.
  //rem
   //The rem unit is similar to em but is always relative to the root element's font-size. This ensures that the size is consistent across the entire document.
  //%
   //The percentage unit is relative to the property it is applied to. For example, if you set the width of an element to 50%, it will be half the width of its containing element.
  //vw and vh:
   //Viewport width (vw) and viewport height (vh) are relative to the size of the viewport (the browser window). 1vw is equal to 1% of the viewport width, and 1vh is equal to 1% of the viewport height.

